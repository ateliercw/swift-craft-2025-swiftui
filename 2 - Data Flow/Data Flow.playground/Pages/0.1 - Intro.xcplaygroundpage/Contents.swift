//: # Data Flow in SwiftUI

/*:

 Moving from [imperative programming](https://en.wikipedia.org/wiki/Imperative_programming) to [declarative programming](https://en.wikipedia.org/wiki/Declarative_programming) for building user interfaces means that how data moves through the views is very different.

 SwiftUI has a structured format for moving that through the UI, and there are defined ways to move data down the view hierarchy to child views, and also pass it back up to parent views.

Things become more complex when you want to pass data data up or down the view hierarchy, and as your application grows having a large number of dependencies that are passed through the environment can add complexity.

